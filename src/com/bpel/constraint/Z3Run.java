package com.bpel.constraint;

import java.io.BufferedInputStream;
import java.io.BufferedReader;
import java.io.File;
import java.io.InputStreamReader;

public class Z3Run {
	private File file=null;
	
	public Z3Run(File file) {
		super();
		this.file = file;
	}
	public void startRun()
	{
		// String cmd = "z3 "+file.getAbsolutePath();  
       //  String cmd="z3 C:\\z3-test-demo.stm2";
    	 String cmd="C:\\Users\\Sun\\Desktop\\相关工作和课程\\bpel\\z3-4.3.2-x64-win\\bin\\z3 "+file.getAbsolutePath(); 

	        //linux  
//	      String cmd = "./fork_wait";  
//	      String cmd = "ls -l";  
//	      String[] cmd=new String[3];  
//	      cmd[0]="/bin/sh";  
//	      cmd[1]="-c";  
//	      cmd[2]="ls -l ./";  
	        Runtime run = Runtime.getRuntime();//返回与当前 Java 应用程序相关的运行时对象  
	        try {  
	            Process p = run.exec(cmd);// 启动另一个进程来执行命令  
	            BufferedInputStream in = new BufferedInputStream(p.getInputStream());  
	            BufferedReader inBr = new BufferedReader(new InputStreamReader(in));  
	            String lineStr;  
	            while ((lineStr = inBr.readLine()) != null)  
	                //获得命令执行后在控制台的输出信息  
	                System.out.println(lineStr);// 打印输出信息  
	            //检查命令是否执行失败。  
	            if (p.waitFor() != 0) {  
	                if (p.exitValue() == 1)//p.exitValue()==0表示正常结束，1：非正常结束  
	                    System.err.println("命令执行失败!");  
	            }  
	            inBr.close();  
	            in.close();  
	        } catch (Exception e) {  
	            e.printStackTrace();  
	        }  
	}
}
